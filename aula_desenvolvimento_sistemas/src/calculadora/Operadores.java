
package calculadora;


public class Operadores {
    
    
    public static double somaValor(double v1, double v2){
        
        return v1+v2;
    }
    
    public static double subValor(double v1, double v2){
        
        return v1-v2;
    }
    
     public static double multValor(double v1, double v2){
        
        return v1*v2;
    }
     
      public static double divValor(double v1, double v2){
        
        return v1/v2;
    }
    
}
